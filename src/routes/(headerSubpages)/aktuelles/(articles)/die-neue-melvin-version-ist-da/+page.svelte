<p>
	Nach mehreren Monaten harter Arbeit, freut sich das Team endlich die neue Melvin Version
	veröffentlichen zu können. Um unser Ziel eines einfachen und nahtlosen Workflows beim Erstellen
	von barrierefreien Videos näher zu kommen, haben wir die folgenden Änderungen vorgenommen:
</p>

<p>
	<strong>Neuer Transkript-Editor</strong>: Der Wandel weg von einzelnen Untertitel-Blöcken hin zu
	einer Fließtextdarstellung ermöglicht ein effizienteres und bequemeres Verbessern des Textes.
	Melvin sorgt nach wie vor dafür, dass die Untertitel an der richtigen Stelle im Video angezeigt
	werden.
</p>

<p>
	<strong>Neue Startseite</strong>: In den kommenden Monaten werden wir diese durch einen Leitfaden
	und durch Tutorials für Melvin erweitern. Bleiben Sie gespannt.
</p>

<strong>Vereinfachte Benutzeroberfläche und so vieles mehr!</strong>

<p>Bitte beachten Sie jedoch noch zwei Punkte:</p>

<p>
	Melvin befindet sich weiterhin in der Entwicklung, es kann also zu Fehlern sowie zu Änderungen der
	Funktionen und Benutzeroberfläche kommen. Wir haben noch viel für die Zukunft geplant. Darüber
	hinaus befindet sich Melvin aktuell noch in einer geschlossenen Testphase. Wenn Sie die Anwendung
	testen möchten und noch keinen Account haben, können Sie uns unter <a
		href="mailto:melvin@shuffle-projekt.de">melvin@shuffle-projekt.de</a
	>
	kontaktieren und wir richten Ihnen dann gerne einen Account ein.
</p>

<p>
	Viel Spaß beim Austesten der neuen Version und wir freuen uns natürlich wie immer über Ihr
	Feedback.
</p>

