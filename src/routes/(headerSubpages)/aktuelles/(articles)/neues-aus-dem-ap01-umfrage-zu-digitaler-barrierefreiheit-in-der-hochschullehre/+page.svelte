<p>
	Heute dürfen wir den offiziellen Start der quantitativen Bedarfserhebung aus dem Arbeitspaket 1
	verkünden!
</p>

<p>
	Unsere Umfrage richtet sich an Studierende und Lehrende: Die Studierendenbefragung verfolgt das
	Ziel, Barrieren in digitalen Lehr-Lernkontexten zu erkennen und damit zusammenhängend individuelle
	Bedarfe zu identifizieren. Mithilfe der Lehrendenbefragung sollen Einsichten in bisherige
	Kenntnisse und Erfahrungen, Weiterbildungswünsche und -bedarfe von Lehrenden gewonnen werden. So
	wird der Stand der digitalen Barrierefreiheit an den Hochschulen/Universitäten aus verschiedenen
	Perspektiven beleuchtet.
</p>

<p>
	Auf unserer Homepage halten wir Sie über Ergebnisse und Erkenntnisse unserer Erhebungen auf dem
	Laufenden!
</p>

<p>
	Die Bedarfsanalyse formt den Grundstein für unsere Vorhaben in den weiteren Arbeitspaketen. Die
	nun beginnende quantitative Erhebung wird zudem bald durch qualitative Interviews mit Lehrenden,
	Leitenden und Studierenden ergänzt, um tiefere Einblicke in die Thematik zu erlangen.
</p>

<p>Wir freuen uns über eine rege Teilnahme an unserer Umfrage!</p>

