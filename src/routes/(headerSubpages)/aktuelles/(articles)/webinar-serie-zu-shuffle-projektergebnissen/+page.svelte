<p>
	Eine Hochschule muss zugänglich für alle sein – aber was bedeutet das für den Studienalltag? Wie
	erleben Studierende mit unterschiedlichen Beeinträchtigungen die Barrierefreiheit an ihrer
	Hochschule und welche hilfreichen Hinweise wollen sie an Lehrende weitergeben? Wir haben mithilfe
	einer großen Datenerhebung und Interviews und Gesprächen mit Studierenden mit Beeinträchtigungen
	diese Erfahrungen gesammelt und sie in sogenannten Personas umgesetzt. Lehrende und Interessierte
	können diese virtuellen Studierenden nun auf BlindDate kennenlernen. Ob genutzte Strategien und
	Technologien, praktische Umsetzungshinweise, allgemeine Informationen zu Barrierefreiheit oder
	auch kritisches Hinterfragen – BlindDate ist eine virtuelle Begegnungsplattform, auf der man in
	die Perspektive der Studierenden eintauchen kann. Besuchen Sie unser Webinar und erfahren Sie
	etwas über den Erstellungsprozess der Personas, die interaktiven Elemente der Webseite und noch
	nicht veröffentlichte Inhalte.
</p>
