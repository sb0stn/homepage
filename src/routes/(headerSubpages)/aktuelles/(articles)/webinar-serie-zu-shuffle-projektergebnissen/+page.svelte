<p>
	Unser Webinar stellt einen neu entwickelten, öffentlich zugänglichen Zertifikatskurs vor, der sich
	speziell an Hochschullehrende und Studienberatende richtet. Es geht darum, digitale
	Barrierefreiheit in der Lehre und beim Lernen zu planen und umzusetzen. Im Webinar gehen wir auf
	die Ziele des Kurses ein und beschreiben die Struktur der vier Module: (1) Grundlagen, (2)
	Vielfalt der Benutzerbedürfnisse, (3) Barrierefreiheit im Hochschulkontext, (4) Tooling Dokumente.
	Wir geben Beispiele für einige praktische Kursinhalte. Zum Erwerb des Zertifikats „Barrierefrei
	lehren“ wird es eine elektronische Prüfung geben. Die Pilotphase für den Zertifikatskurs ist ab
	Herbst 2024 geplant.
</p>

