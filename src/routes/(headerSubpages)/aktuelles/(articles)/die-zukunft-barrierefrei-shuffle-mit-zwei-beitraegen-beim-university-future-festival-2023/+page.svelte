<p>
	Auch beim diesjährigen <span lang="en">University Future Festival</span> war das SHUFFLE-Team mit
	diesmal zwei <span lang="en">Lightning-Talks</span> vertreten. In der Session Barrieren wahrnehmen
	und abbauen stellten zuerst Ann-Katrin Böhm aus Heidelberg und Christin Stormer aus Bielefeld für das
	AP01 die Entwicklung eines Reifegradmodells vor, das Hochschulen helfen soll, den Stand der Barrierefreiheit
	in ihrer Einrichtung zu erheben und auszubauen.
</p>

<p>
	Samira Kalemba und Hakan Cetin (beide Freiburg) veranschaulichten im Anschluss exemplarisch anhand
	der Errichtung barrierefreier Arbeitsplätze an der PH Freiburg, wie Barrierefreiheitsmaßnahmen
	kostengünstig und effizient umgesetzt werden können. Obwohl zeitgleich mit Raul Krauthausen einer
	der prominentesten Aktivisten für Inklusion und Barrierefreiheit auf der <span lang="en"
		>Main Stage</span
	> auftrat, nahmen etwa vierzig Personen an der Session teil, von denen einige im Anschluss die Gelegenheit
	nutzten, an den Netzwerktischen Fragen zu stellen und sich auszutauschen. Auch vor Ort in Berlin ergaben
	sich interessante Gespräche über die Zukunft des deutschen Hochschulwesens, die Arbeit des Projektes
	und die Bedeutung von Teilhabemöglichkeiten.
</p>

<p>
	Vielen Dank allen Speaker*innen und Organisator*innen des <span lang="en"
		>University Future Festival</span
	> 2023, dass sie dieses wertvolle und umfangreiche Format ermöglichten. Wir nehmen sehr viele spannende
	Eindrücke und Ideen für unsere weitere Arbeit mit, und hoffen, dass auch wir mit unseren Talks und
	im persönlichen Gespräch Anregungen schaffen und die Schwelle im Einsatz für eine inklusivere Hochschule
	etwas absenken konnten. Für alle Interessierten stehen die Aufzeichnungen zahlreicher Sessions und
	Talks in Kürze auf dem YouTube-Kanal des Hochschulforum Digitalisierung zur Verfügung.
</p>

