<p>
	Bereits seit Ende 2022 laufen im Begleitprogramm zur Projektförderung der Stiftung Innovation in
	der Hochschullehre (StIL) hochwertige Weiterbildungen für Projektleitungen und -mitarbeitende.
	Auch einige unserer Teammitglieder durften sich über die begehrten Teilnahmeplätze freuen.
</p>

<p>
	Am 7. Und 8. September fand nun in Kooperation zweier dieser Weiterbildungen eine Summer School in
	der Essener Philharmonie statt. Über zwei Tage konnten wir in verschiedenen Workshops unter
	anderem Techniken der lateralen Führung, Systemische Strukturaufstellungen, intensive
	Biografiearbeit und sogar (sanfte) Lektionen des Boxsports für die Karriereentwicklung
	kennenlernen und erproben.
</p>

<p>
	Besonders gefreut hat sich das kleine SHUFFLE-Team neben dem vielseitigen Workshopprogramm über
	das Präsenzformat. So bot sich, nach über einem halben Jahr der Online-Zusammenarbeit, endlich
	eine Gelegenheit die Kolleg*innen aus vielen anderen Stiftungsprojekten auch persönlich
	kennenzulernen und zum Beispiel bei Kaffee und Kuchen anregende Gespräche zu führen.
</p>

<p>
	Wir bedanken uns bei Alexandra Busch und Mareike Menne und ihren jeweiligen Teams für die
	ausgezeichnete Organisation und freuen uns auf die weiteren Weiterbildungstermine und viele
	spannende Einsichten bis Ende des Jahres.
</p>
