<p>
	Im Rahmen vom SHUFFLE entwickeln Studierende im Software-Gruppen-Projekt der Uni Bielefeld ein
	Prüftool zur Testung von Lernräumen in Lernmanagementsystemen auf Barrierefreiheit.
</p>

<p>
	Dabei treten 10 Teams bestehend aus 8-12 Studierende gegeneinander an. Das beste Prüftool wird bei
	der Preisverleihung am Ende des Sommersemesters 2022 gekürt und das Gewinnerteam erhält für die
	beste Idee und Umsetzung einen Geldpreis.
</p>

<p>
	Hintergrund der Aufgabenstellung ist, dass es zwar bereits Prüftools zur Testung von Websites auf
	Barrierefreiheit gibt, jedoch kaum welche speziell für Lernraumsysteme. Zudem sind diese Tools
	lernsystemabhängig und zur vollumfänglichen Nutzung kostenpflichtig. Damit Dozierende für Ihre
	Studierende einen möglichst barrierefrei gestalteten Lernraum anbieten können, soll das
	entstehende Prüftool unter einer <span lang="en">Open-Source</span>-Lizenz zur Nutzung zur
	Verfügung gestellt werden.
</p>
