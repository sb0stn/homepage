<h2>Transkript zum Erklärvideo</h2>

<p>
	Dieses Video ist eine Einführung in das Modell des <span lang="en"
		>"Universal Design for Learning".</span
	>
	Die Abkürzung dafür ist "UDL".
</p>

<p>
	Wir stellen grundlegende Prinzipien und Annahmen des "UDL" vor. Für eine vollständige
	Vorstellung ist das Modell jedoch zu komplex.
</p>

<p>
	Das <span lang="en">"Universal Design for Learning"</span> basiert auf dem Modell
	<span lang="en">"Universal Design"</span>.
</p>

<p>Auf Deutsch heißt das "Design für Alle".</p>

<p>
	<span lang="en">"Universal Design"</span> kommt ursprünglich aus
	der Architektur. Ziel in der Architektur ist die Gestaltung von Kaufhäusern und öffentlichen
	Orten, sodass möglichst alle Personen diese Orte nutzen können.
</p>

<p>
	Das
	<span lang="en"> "Universal Design for Learning" </span>
	überträgt diesen Gedanken auf Curricula. Es geht nicht mehr um physisch existierende Orte, sondern um Lernen, Lehrpläne und Lerninhalte.
</p>

<p>Curricula sollen alle Lernenden in ihrem Lernprozess unterstützen.</p>

<p>
	Verschiedene Lernprozesse von verschiedenen Menschen finden bereits in der Konzeption
	Berücksichtigung. So sind individuelle Anpassungen im Nachhinein nur selten notwendig.
	Das gilt auch für Medienwelten: Sie sollen von Beginn an für Alle zugänglich sein.
</p>

<p>Wenn wir Curricula mit dem "UDL" betrachten, achten wir auf Folgendes:</p>

<p>Wo sind Barrieren oder Probleme für das Lernen?</p>

<p>Finden verschiedene Voraussetzungen Berücksichtigung?</p>

<p>Wo gibt es Alternativen in der Gestaltung von Lerninhalten?</p>

<p>
	Als nächstes stellen wir die verschiedenen Ebenen des <span lang="en"
		>„Universal Design for Learning“</span
	>
	in Anlehnung an CAST vor.
</p>

<p>Das Modell ist als Tabelle mit drei Spalten dargestellt. Jede Spalte steht für eine Ebene.</p>

<p>Die erste Ebene heißt Lernengagement. Sie findet sich in einer grünen Spalte.</p>

<p>
	Diese Ebene betrachtet, WARUM wir lernen. Es geht darum, das Lerninteresse und die
	Motivation der*des Lernenden zu fördern.
</p>

<p>
	Medien können dies zum Beispiel durch vielfältige, interaktive Elemente leisten. Sie setzen
	außerdem an Erfahrungen aus dem Alltag an.
</p>

<p>Die zweite Ebene heißt Informationen. Sie findet sich in einer lila Spalte.</p>

<p>Diese Ebene betrachtet, WAS wir lernen.</p>

<p>
	Es geht darum, Informationen darzustellen und wahrzunehmen. Medien können hierbei
	die Wahlmöglichkeiten der Darstellung erweitern.
</p>

<p>
	Die dritte Ebene heißt Informationsverarbeitung und Interaktion mit Lerngegenständen. Sie findet
	sich in einer blauen Spalte.
</p>

<p>
	Diese Ebene betrachtet, WIE wir lernen. Es geht darum, mit Lerngegenständen zu interagieren und
	den Prozess des Lernens zu unterstützen.
</p>

<p>
	Mit Hilfe von Medien erweitern sich die Wege der Kommunikation und die Möglichkeiten,
	Transparenz zu schaffen.
</p>

<p>Nun gibt es zwei Arten die Tabelle des Modells zu lesen.</p>

<p>
	Bei Möglichkeit 1 liegt der Fokus auf einer Förderung der drei verschiedenen Ebenen, die
	wir zuvor in den drei verschiedenen Farben vorgestellt haben. Wir lesen jede Spalte, also jede
	Farbe, für sich und von oben nach unten.
</p>

<p>Bei Möglichkeit 2 liegt der Fokus auf einem Lernprozess von außen nach innen.</p>

<p>
	Dazu lesen wir die Tabelle von links nach rechts. Dadurch sind alle drei farbigen
	Ebenen gleichzeitig im Blick.
</p>

<p>Zuerst sehen wir die Ebene des Zugangs.</p>

<p>
	Hier geht es dann vor allem um das Äußere. Wie gestalte ich zum Beispiel die Lernumgebung,
	die Lerninhalte oder die eingesetzten Medien?
</p>

<p>
	Dann kommt die Ebene der Entwicklung: Wie bleiben Lernende im Lernprozess?
	Wie unterstützen Medien den Ausdruck und die Kommunikation?
</p>

<p>
	Ganz unten in der Tabelle ist die Ebene der Verinnerlichung. Unten steht also das Innere des
	Lernens.
</p>

<p>
	Hierbei geht es darum: Wie können Lernende ihren Lernprozess selbstständig organisieren
	und strukturieren? Wie unterstützen Medien das Verständnis?
</p>

<p>Zuletzt steht eine Zusammenfassung. Ziel ist die Entwicklung von Lernexpert*innen.</p>

<p>
	Lernexpert*innen sind Lernende, welche die Umsetzung des "UDL" beherrschen und auf den jeweiligen
	Ebenen folgende Eigenschaften besitzen:
</p>

<p>Lernexpert*innen sind auf der Ebene des Lernengagements zielstrebig und motiviert.</p>

<p>Lernexpert*innen sind auf der Ebene der Informationen einfallsreich und sachkundig.</p>

<p>Lernexpert*innen sind auf der Ebene der Informationsverarbeitung strategisch und zielführend.</p>

<p>
	Um diese Lernexpert*innen zu erreichen, lesen wir die Tabelle vom "UDL" immer von oben nach
	unten und gleichzeitig auch von links nach rechts.
</p>

<p>Denn: Die oberen Ebenen sind immer Voraussetzung, um das Untere zu erreichen.</p>

<p>
	Beispiel: Wenn die Lernumgebung nicht sinnvoll und ansprechend gestaltet ist, lernen die Lernenden
	nicht, den Lernprozess selbstreguliert zu gestalten.
</p>

<p>Das war ein erster Einblick in das Universal Design for Learning.</p>

<p>
	Wenn du noch mehr über das "UDL" wissen möchtest, besuche die Website der UDL Guidelines: <a
		href="https://udlguidelines.cast.org">https://udlguidelines.cast.org</a
	>.
</p>

