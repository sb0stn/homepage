<p>
	Vom 13. bis zum 15. Juli 2022 fand die gemeinschaftliche ICCHP-AAATE Konferenz in Lecco, Italien
	statt. Digitale Barrierefreiheit und assistive Technologien standen dabei im Mittelpunkt für
	Forschende und Fachkräfte, die ihre Arbeiten präsentieren und sich untereinander austauschen und
	vernetzen konnten. Auch SHUFFLE war vor Ort dabei und gestaltete das Konferenzprogramm mit einem
	Paper und drei Posterpräsentationen mit:
</p>

<ul>
	<li>
		Paper: <span lang="en"
			>Evaluating Collaborative Editing of AI-Generated Live Subtitles by Non-Professionals in
			German University Lectures</span
		> (AP04)
	</li>
	<li>
		Poster: <span lang="en">MELVIN: Multi-user editor for creating and editing live captions </span>
		(AP04)
	</li>
	<li>
		Poster: <span lang="en"
			>Using Design Thinking Methods in Developing a Digital Accessibility Resource Website
		</span> (AP06)
	</li>
	<li>
		Poster: <span lang="en">
			An Analysis of the Accessibility Issues of the Learning Management System ILIAS
		</span> (AP09)
	</li>
</ul>

<p>
	Mitarbeitende aller Partnerhochschulen des Projekts kamen in einer Ferienwohnung in der Nähe des
	Veranstaltungsortes für eine Woche zusammen. Am Montag und Dienstag stand jeweils der fachliche
	Austausch innerhalb des Teams in Form von Workshops im Vordergrund, bevor wir von Mittwoch bis
	Freitag am Konferenzprogramm teilnehmen konnten. Bei weit über 30°C Außentemperatur durfte auch
	das eine oder andere gemeinsame Schwimmengehen im Comer See nach einem langen Arbeitstag nicht
	fehlen.
</p>

<p>
	Mit vielen wertvollen Kontakten, vielschichtigen Eindrücken und neuer Motivation für das Thema der
	digitalen Barrierefreiheit sind wir nun zurück in Deutschland.
</p>
