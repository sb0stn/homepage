<p>
	Digitale Lehre nachhaltig zu gestalten ist nicht erst seit der Coronapandemie ein zentrales
	Anliegen der Universität Bielefeld. Bereits 2018 stößt die Universität einen umfangreichen
	Strategieprozess an, in dem Lehrende und Studierende gemeinsam Eckpunkte erarbeiten, wie sie sich
	langfristig Lehr- und Lernsettings an der Universität vorstellen können. Darunter auch Ideen zu
	einer barrierefreien Gestaltung von Hochschullehre.
</p>

<p>
	Welche Schlüsse aus den Erfahrungen von Lehrenden und Studierenden für die zukünftige
	(barrierefreie) Gestaltung von Lehre an der Universität Bielefeld gezogen werden, erklärt
	Professorin Dr. Birgit Lütje-Klose, Prorektorin für Studium und Lehre, im Interview mit Campus TV
	der Universität Bielefeld. Hier weist sie ab Minute 05:20 auch auf das SHUFFLE-Projekt hin.

