<p>
	Als Teil von Arbeitspaket 6 möchten wir Personas gestalten, die Barrieren im Studium sichtbar
	machen und Strategien der Studierenden aufzeigen. Natürlich können wir das nicht alleine – deshalb
	haben wir am Freitag, den 25.02.2022 zu einem Design Thinking Workshop eingeladen, um die
	Grundlagen für unsere Personas zu legen. Wir waren insgesamt 12 Teilnehmende, darunter
	Expert*Innen für Sehbeeinträchtigung, Hörbeeinträchtigung, psychische Beeinträchtigungen,
	körperliche Beeinträchtigungen und chronischen Erkrankungen.
</p>

<p>
	Als Vorbereitung auf den Workshop hatten wir die Aufgabe, sowohl typische Barrieren im Studium
	festzuhalten, als auch Situationen zu beschreiben, in denen alles gestimmt hat. Diese Situationen
	haben wir gemeinsam festgehalten. Direkt im Workshop haben wir dann auch direkt die erste Barriere
	erlebt: die Arbeit mit neuen digitalen Tools wie Mural, blink.it und Zum-Pad. Gemeinsam haben wir
	es aber geschafft, diese Barriere aus dem Weg zu räumen und uns gegenseitig bei der Orientierung
	und Dokumentation der Ergebnisse zu unterstützen. Die offene Atmosphäre im Workshop hat uns
	erlaubt, diese Probleme direkt zu adressieren und war auch die Grundlage für einen offenen,
	vertrauensvollen Austausch.
</p>

<p>
	Nach einer wohlverdienten Pause ging es am Nachmittag weiter. In Expert*Innen-Gruppen machten wir
	uns daran, ein Grundgerüst für unterschiedliche Personas zu erarbeiten. Dabei halfen uns Leifragen
	wie z.B. „Welche Superkraft hat deine Persona“ oder „Was macht eine gute Lernatmosphäre aus?“. In
	einer weiteren Runde gab es einen Perspektiv-Wechsel und die Personas wurden aus Sicht der
	Lehrenden interviewt. Der Erfahrungsaustausch im Workshop war unglaublich wertvoll und hat uns
	viele neue Erkenntnisse geliefert. Und natürlich sind wir unseren Personas wieder ein ganzes Stück
	näher gekommen. Ein ganz herzliches Dankeschön an alle, die mitgemacht haben und ihre Erfahrungen
	so offen geteilt haben!
</p>
