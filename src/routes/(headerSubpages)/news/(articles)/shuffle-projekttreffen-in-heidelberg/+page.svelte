<p>
	Im Oktober stand unser nächstes gemeinsames SHUFFLE-Projekttreffen an, das wir dieses Mal in
	Heidelberg durchgeführt haben. Zwei Tage konnten wir uns beim Herbsttreffen über den aktuellen
	Stand der verschiedenen Arbeitspakete informieren, Pläne für die noch vor uns liegende Zeit im
	Projekt machen und in einigen Workshops an konkreten Aufgabestellungen arbeiten.
</p>

<p>
	Der erste Tag bestand aus verschiedenen Workshops, in denen wir an konkreten Aufgabestellungen
	gearbeitet haben. So konnten wir uns im Ethik-Workshop des Arbeitspakets 11 damit beschäftigen,
	welche Werte wir in unsere Arbeit bereits eingebracht haben und welche Werte uns auch weiterhin in
	unserer Arbeit für SHUFFLE begleiten sollen. Am Nachmittag hatten wir die Möglichkeit, ganz im
	Sinne von Arbeitspaket 12, mal ganz konkret in unseren verschiedenen Arbeitspaketen zu besprechen,
	welche Ziele wir auf jeden Fall noch bis zum Ende der Laufzeit des Projekts erreichen müssen, um
	uns so auf den letzten Streckenabschnitt vorzubereiten. Zum Ende des Tages konnten wir noch
	weitere Themen, die uns wichtig waren, in einer offenen Diskussionsrunde besprechen.
</p>

<p>
	Am Abend bekamen dann die Heidelberg-externen SHUFFLE-Mitarbeitenden eine kleine Führung zum
	Heidelberger Schloss mit einigen interessanten Informationen zu Heidelberg. Den Tag konnten wir
	bei einem gemeinsamen Abendessen ausklingen lassen, um dann für den nächsten Tag wieder gestärkt
	zu sein.
</p>

<p>
	Den zweite SHUFFLE-Tag haben wir dafür genutzt, zunächst in einer Poster-Session alle im Team über
	die bereits erreichten Ziele in den verschiedenen Arbeitspaketen zu informieren und über das
	weitere Vorgehen zu diskutieren. In einer offenen Runde mit den Postern je Arbeitspaket konnten
	wir uns so auf den aktuellen Stand bringen und Feedback an die anderen Arbeitspakete geben.
	Anschließend haben wir gemeinsam in einem Workshop des Reifegradmodell-Teams an der
	Weiterentwicklung des Reifegradmodells gearbeitet, bevor wir uns für den Nachmittag wieder in die
	Arbeitspakete aufgeteilt haben und die Zeit für gemeinsam anstehende Aufgaben genutzt haben.
</p>

<p>
	Alles in allem hat uns dieses Herbsttreffen frischen Aufwind gegeben und uns unsere Ziele für die
	restliche Projektlaufzeit noch mal klar werden lassen. Mit neuer Motivation und gestärktem
	Teamgeist konnten wir damit in die nächste Arbeitsphase starten.
</p>

