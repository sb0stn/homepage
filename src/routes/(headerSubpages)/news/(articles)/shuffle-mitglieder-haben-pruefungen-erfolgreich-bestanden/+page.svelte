<ArticleHeader
	title="SHUFFLE-Mitglieder haben Prüfungen erfolgreich bestanden"
	authorName="Dr. Kathy-Ann Heitmeier"
	date="20.10.2022"
/>

<p>
	Zwischen Oktober 2021 und Oktober 2022 haben acht Mitglieder des SHUFFLE-Teams Prüfungen zur
	digitalen Barrierefreiheit geschrieben und bestanden, die von der
	<span>
		<a class="blue" href="https://iaap-dach.org/">
			International Association of Accessibility Professionals
		</a>
	</span>
	(IAAP) abgenommen wurden. Sieben Teammitglieder erwarben das
	<span>
		<a href="https://iaap-dach.org/CPACC.html" class="blue"> CPACC-Zertifikat </a>
	</span>
	(Certified Professional in Accessibility Core Competencies), eine grundlegende Zertifizierung, die
	ein breites Wissen über Barrierefreiheit, universelles Design und verwandte Standards und Strategien
	vermittelt. Ein Teammitglied erhielt die CPWA-Zertifizierung, nachdem es sowohl die CPACC-Prüfung als
	auch die Prüfung zum
	<span>
		<a class="blue" href="https://iaap-dach.org/zertifizierungen/was.html">
			Web Accessibility Specialist
		</a>
	</span>
	(WAS) auf technischer Ebene bestanden hatte. Die Prüfungen können auf Englisch oder auf Deutsch abgelegt
	werden, wobei das
	<span>
		<a class="blue" href="http://kdb.hdm-stuttgart.de/">
			Kompetenzzentrum für digitale Barrierefreiheit
		</a>
	</span>
	an der Hochschule der Medien eines der Prüfungszentren für deutschsprachige Prüfungen in der DACH-Region
	ist. Wir freuen uns auf die nächste Gruppe von SHUFFLE-Mitgliedern, die Anfang 2023 die Prüfungen ablegen
	werden.
</p>
