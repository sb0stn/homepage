<p>
	Nach einer längeren Präsenzpause aufgrund der Corona-Situation haben wir es am 08. und 09.
	September geschafft, uns zu unserem einjährigen Jubiläum wieder in Präsenz in Stuttgart zu
	treffen. Wir haben die zwei Tage gemeinsam intensiv genutzt, um inhaltlich Themen zu besprechen
	und das letzte Jahr rekapitulieren zu lassen. Zudem konnten wir in dieser Zeit auch im Team noch
	stärker zusammenwachsen und die vielen neuen Mitarbeitenden persönlich kennenlernen und willkommen
	heißen. Im Folgenden ein paar Eindrücke, was wir in diesen zwei intensiven Tagen alles erarbeiten
	konnten.
</p>

<p>
	Das Reifegradmodell ist eines der Ziele im SHUFFLE-Projekt, das alle Arbeitspakete am Ende
	verbinden soll. Deshalb war das Präsenztreffen eine gute Möglichkeit, um möglichst viel Expertise
	in einem Raum zu bündeln und wichtige Grundlagen für das Reifegradmodell zu schaffen. Wir haben in
	dem Workshop verschiedene Metriken erörtert und diskutiert, die im Reifegradmodell enthalten sein
	sollen.
</p>

<p>
	Mit der Frage „Bist Du blind oder ein Jogger?“ zum Einstieg haben wir uns als letzten
	Programmpunkt noch einmal mit dem Begriff der Studierenden mit individuellen Bedarfen (SmiB)
	auseinandergesetzt und in einer aufschlussreichen Diskussion über Bedarfe und deren Definition
	gesprochen.
</p>

<p>
	Zum Abschluss unseres ersten Tages haben wir uns auf eine Entdeckungsreise zurück in die
	Vergangenheit gemacht. Das erste Jahr unseres Projektes ist nun vorbei und wir haben uns
	angeschaut, welche Meilensteine wir bereits erreicht haben und welche Höhepunkte und Tiefpunkte
	wir gemeinsam erlebt haben. Gemeinsam konnten wir mit diesem Wissen auch noch einen Ausblick auf
	die noch folgenden zwei Jahre werfen. Hier lag vor allem im Fokus, welche Ziele wir noch erreichen
	wollen und wie wir das am besten schaffen.
</p>
