<p>
	Letzte Woche haben wir im Arbeitspaket 02 spannende Gespräche mit verschiedenen Expert*innen aus
	ganz Deutschland geführt.
</p>

<p>
	Im ersten Gespräch haben wir vielfältige Einblicke in die Tätigkeiten unserer Expertinnen aus der
	Beratung von Studierenden v.a. mit chronischer Krankheit und Behinderung erhalten. Es ging sowohl
	um das Angebot, was sie den Studierenden machen (können), als auch um Hindernisse, die sich ihnen
	regelmäßig in den Weg stellen. Wir haben erfahren können, welche Netzwerke für eine erfolgreiche
	Beratung und Begleitung nützlich oder sogar notwendig sind. Gleichzeitig kamen wir aber auch über
	Hoffnungsschimmer und Wünsche für die zukünftige Verankerung und Gestaltung von Beratung
	Studierender ins Gespräch.
</p>

<p>
	Für das zweite Gespräch konnten wir Expert*innen für Hochschulorganisation und
	Strategieentwicklung gewinnen. Die Diskussion behandelte die strategische Verankerung der
	digitalen Barrierefreiheit an den Hochschulen im Detail. Wir haben hierbei neue Einblicke in die
	Akteure und Stakeholder der Strategieentwicklung gewonnen und uns näher mit dem Thema der
	Ressourcen-Beschaffung und -Umverteilung zur Stärkung der digitalen Barrierefreiheit beschäftigt.
	Darüber hinaus konnten wir durch eine Sammlung der Erfolgskriterien für die strategische
	Verankerung die wichtigsten Handlungsfelder identifizieren.
</p>

<p>
	An dieser Stelle nochmal ein großes Dankeschön an alle Teilnehmenden! Der Austausch hat uns
	wichtige Einblicke in verschiedene Bereiche zur strategischen Weiterentwicklung von digitaler
	Barrierefreiheit in Hochschulen gegeben.
</p>
