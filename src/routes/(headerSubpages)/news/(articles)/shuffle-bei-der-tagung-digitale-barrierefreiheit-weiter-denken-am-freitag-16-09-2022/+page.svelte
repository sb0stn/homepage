<p>
	Das Netzwerk digitale Hochschullehre Hessen hat am Freitag, dem 16.09.22 eingeladen, innovative
	Projekte, spannende Konzepte zur barrierefreien, digitalen Lehre und alle anderen Aktivitäten rund
	um die Barrierefreiheit in der digitalen (Hochschul-) Bildung vorzustellen und zu diskutieren.
</p>

<p>
	Das SHUFFLE Team war reichlich vertreten. In zwei Vorträgen und einem Workshop haben wir unsere
	Erfahrungen und Ergebnisse aus dem letzten Jahr vorstellt und diskutiert.
</p>

<p>Folgende Beiträge konnten wir erfolgreich in die Tagung einbringen:</p>

<strong>Vortrag: Barrieren in Moodle: Welche gibt es und was können wir tun?</strong>

<p>
	In einer Studie wurde ein exemplarischer Moodle-Kurs nach den Kriterien des BIK BITV-Tests auf
	Barrierefreiheit überprüft. Wir haben einige interessante Ergebnisse präsentiert und haben mit den
	Teilnehmenden diskutiert, wie Fehler seitens Kursersteller*innen vermieden werden können.
</p>

<strong>
	Vortrag: „Was ich eigentlich gebraucht hätte…“ – Wege der Kommunikation Studierender bei digitalen
	Barrieren
</strong>

<p>
	Anfang 2022 hat SHUFFLE an den Verbundhochschulen eine Befragung zur digitalen Barrierefreiheit
	durchgeführt. Ziel war es, Studierende mit individuellen Bedarfen (Studierende mit Erschwernissen
	im Studium aufgrund permanenter, situativer oder temporärer Umstände) sowie Lehrende zu ihren
	Erfahrungen mit digitaler Lehre zu befragen. Die vorgestellten Ergebnisse zeigen eine
	Dringlichkeit des Themas, weil Supportstrukturen (technischer Support, allgemeine Beratung, usw.)
	offensichtlich noch zu wenig vorhanden bzw. transparent sind. Oftmals sind Studierenden, die auf
	digitale Barrieren stoßen, mögliche Ansprechpartner gar nicht bekannt.
</p>

<strong>
	Workshop: Digitale Barrierefreiheit fest verankert – eine Werkstatt zur Zukunft unserer Hochschule
</strong>

<p>
	In einer kurzen und intensiven Zukunftswerkstatt (nach Robert Jungk) wurden Wege und Strategien
	für eine zukunftsfähige Verankerung von digitaler Barrierefreiheit an Hochschulen entwickelt. In
	drei Arbeitsphasen wurden das Wissen und die Erfahrungen der Teilnehmenden von verschiedenen
	Hochschulen zusammengetragen. Zudem wurde Raum für Visionen und soziale Fantasien zum Entfalten
	gegeben. So sollten neben den Hindernissen auf dem Weg zur Verwirklichung einer barrierefreien
	Hochschulzukunft auch bereits existierende Ansätze und Good Practice-Beispiele identifiziert und
	weiterentwickelt sowie innovative Zukunftsentwürfe geschaffen werden.
</p>

