<p>SHUFFLE auf der CHI 2023.</p>

<p>
	Vier unserer SHUFFLElistas (Nadine, Verena, Kathy, Kobi) waren in Hamburg auf einer der größten
	und wichtigsten internationalen Konferenzen in der Informatik: Der CHI (“ACM CHI Conference on
	Human Factors in Computing Systems”). Die weltweite Forschung im Bereich Mensch-Maschine
	Interaktion ist dort vertreten und präsentiert neueste Ergebnisse zu Themen wie Usability,
	User-Experience und natürlich auch künstlicher Intelligenz.
</p>

<p>
	Wir freuen uns sehr, dass wir mit SHUFFLE gleich zwei Publikationen auf der CHI veröffentlichen
	konnten. Die beiden Paper sind in der ACM Digital Library abrufbar. Zudem wurden diese auf der
	Konferenz als Poster präsentiert und wir konnten uns dabei in interessanten Gesprächen mit anderen
	Wissenschaftler*innen über unsere Forschung austauschen.
</p>

<p>
	Aus Arbeitspaket 6 haben (Kathy-Ann, Verena, Patricia, Ann-Katrin, Niklas, Markus und Gottfried)
	den Prozess des Persona Co-Designs vorgestellt. Das Paper mit dem Titel “Persona Co-Design for
	Improving Digital Accessibility” ist unter folgendem Link in der ACM Digital Library abrufbar:
</p>

<a href="https://dl.acm.org/doi/10.1145/3544549.3585857" class="blue"
	>https://dl.acm.org/doi/10.1145/3544549.3585857</a
>

<p>
	Im Rahmen des Arbeitspakets 4 haben (Kobi, Verena, Gottfried) den Prototyp des Untertitel-Editors
	in einem Nutzertest evaluiert und Ergebnisse zum kollaborativen Workflow ausgewertet: Das Paper
	mit dem Titel “Accuracy of AI-generated Captions With Collaborative Manual Corrections in
	Real-Time” ist unter folgendem Link in der ACM Digital Library abrufbar:
</p>

<a href="https://dl.acm.org/doi/10.1145/3544549.3585724" class="blue"
	>https://dl.acm.org/doi/10.1145/3544549.3585724</a
>

<p>
	Wir nehmen mit: Die Eindrücke spannender Vorträge; die persönlichen Gespräche mit Forscher*innen
	aus aller Welt; Das SHUFFLE mit dem Thema digitale Barrierefreiheit hochaktuell ist.
</p>
